public class Variaveis {
    public static void main (String[] args){
        
        
        String nome = "Gabriel", sobrenome = "Mendes"; //a String é nada mais nada menos que uma 'linha', uma coisa que voce escreve pra usar la na frente.
        
        int idade = 18; //o int são as famosas variaveis de números naturais (não tem vírgula, só número completo!).
        
        double altura = 1.70; //o double é a famosa variavel de número real (pode ter vírgula, pode ser número quebrado!).
        
        
        System.out.println (nome + " " + sobrenome + ", voce tem" + " " + idade + " " + "anos" + " " + "e" + " " + altura + " " + "de altura.");
    }
}

